export type UserManagementCopy = (typeof userManagementCopy)[AppLocale]

export function getUserManagementCopy(locale: AppLocale): UserManagementCopy {
  return userManagementCopy[locale] ?? userManagementCopy.en
}

export function usernameIssueMessage(copy: UserManagementCopy['validation'], issue: UsernameIssue): string {
  switch (issue) {
    case 'empty':
      return copy.usernameEmpty
    case 'too_short':
      return copy.usernameShort
    case 'too_long':
      return copy.usernameLong
    case 'invalid_chars':
      return copy.usernameChars
  }
}

export function passwordIssueMessage(copy: UserManagementCopy['validation'], issue: PasswordIssue): string {
  if (issue === 'too_long') return copy.passwordLong
  return copy.passwordShort
}
